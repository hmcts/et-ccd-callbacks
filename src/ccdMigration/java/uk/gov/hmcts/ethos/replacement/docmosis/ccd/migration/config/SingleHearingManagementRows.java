    static void configureComplexTypeAccess(
            ConfigBuilder<CaseData, CaseState, SingleRole> builder,
            SingleDefinitionSupport.Variant variant) {
        if (variant.includes(SCOTLAND)) {
            grantHearingElements(builder, COMMON_HEARING_ELEMENTS, Permission.CRU);
            grantHearingElements(builder, SCOTLAND_HEARING_ELEMENTS, Permission.CRU);
        } else {
            grantHearingElements(builder, COMMON_HEARING_ELEMENTS, READ);
            grantHearingElements(builder, ENGLAND_WALES_HEARING_ELEMENTS, READ);
            grantHearingDetailsElements(builder, ENGLAND_WALES_HEARING_DETAILS_READ_ELEMENTS, READ);
            grantHearingDetailsElements(
                    builder, ENGLAND_WALES_HEARING_DETAILS_DELETE_ELEMENTS, DELETE);
        }
    }

    private static void grantHearingElements(
            ConfigBuilder<CaseData, CaseState, SingleRole> builder,
            String[] elements,
            Set<Permission> solicitorPermissions) {
        for (String element : elements) {
            builder.grantComplexType(
                    CaseData::getHearingCollection,
                    element,
                    Permission.CRU,
                    SingleRole.CASEWORKER_EMPLOYMENT);
            builder.grantComplexType(
                    CaseData::getHearingCollection,
                    element,
                    solicitorPermissions,
                    SingleRole.CASEWORKER_EMPLOYMENT_LEGALREP_SOLICITOR);
        }
    }

    private static void grantHearingDetailsElements(
            ConfigBuilder<CaseData, CaseState, SingleRole> builder,
            String[] elements,
            Set<Permission> permissions) {
        for (String element : elements) {
            builder.grantComplexType(
                    CaseData::getHearingDetailsCollection,
                    element,
                    permissions,
                    SingleRole.CASEWORKER_EMPLOYMENT_LEGALREP_SOLICITOR);
        }
    }

